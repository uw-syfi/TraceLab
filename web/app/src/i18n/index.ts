export function copyFor(locale: unknown) {
  return uiCopy[normalizeLocale(locale)];
}

export function useTranslations(url: URL | { pathname: string }) {
  return copyFor(localeFromUrl(url));
}

export function localizedHome(locale: Locale): string {
  return locale === 'zh' ? '/zh/' : '/';
}

export function alternateHome(locale: Locale): string {
  return locale === 'zh' ? '/' : '/zh/';
}

export function template(text: string, values: Record<string, string | number>): string {
  return text.replace(/\{(\w+)\}/g, (_, key) => String(values[key] ?? ''));
}
